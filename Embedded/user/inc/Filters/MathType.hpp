/**
 * @brief   Концепт, проверяющий поддержку базовых математических операций.
 * @tparam  T   Проверяемый тип.
 * @details Требует наличия следующих операторов:
 *          - сложение двух объектов T (a + b);
 *          - вычитание двух объектов T (a - b);
 *          - умножение объекта T на скаляр типа float (a * f);
 *          - умножение двух объектов T (a * b);
 *          - деление двух объектов T (a / b).
 *          А также аналогичные операции с присваиванием.
 * 
 * @note    Концепт используется для ограничения шаблонных параметров в классах,
 *          выполняющих статистические вычисления и фильтрацию (например, Stats,
 *          NSigmaFilter). Позволяет отловить ошибки несовместимости типов на этапе
 *          компиляции.
 */
template<typename T>
concept MathType = requires(T a, T b, float f) {
    // Проверка наличия необходимых конструкторов
    T();    // Конструктор по умолчанию
    T(f);   // Конструктор от float
    a = b;  // Оператор присваивания

    // Проверка наличия математических операций без присваивания
    a + b;
    a - b;
    a * b;
    a / b;
    a * f;
    a / f;

    // Проверка наличия математических операций с присваиванием
    a += b;
    a -= b;
    a *= b;
    a /= b;
    a *= f;
    a /= f;
};

#endif /*   MATH_TYPE_HPP   */