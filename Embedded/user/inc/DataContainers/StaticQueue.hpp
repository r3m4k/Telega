/** ****************************************************************************
 * @file StaticQueue.hpp
 * @brief Реализация статической циклической очереди фиксированного размера
 * 
 * Этот модуль предоставляет шаблонный класс для работы с FIFO-очередью,
 * использующий статическое выделение памяти. Класс предназначен для
 * встраиваемых систем с ограниченными ресурсами, где динамическое
 * выделение памяти недопустимо.
 * 
 * @section Особенности реализации
 * - Статическое выделение памяти во время компиляции
 * - Проверка требований к типу элементов на этапе компиляции
 * - Поддержка только операций копирования (без перемещения)
 * 
 * @version 1.0.0
 * @date Январь 2026
 * @author Романовский Роман
 **************************************************************************** */

/* Includes ------------------------------------------------------------------*/
#include <type_traits>
#include <stdint.h>

