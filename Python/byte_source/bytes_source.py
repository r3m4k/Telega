# -*- coding: utf-8 -*-
"""Модуль для работы с источниками байтовых данных.

Предоставляет абстрактный базовый класс `BytesSource`, определяющий интерфейс
для чтения байтов из различных источников (COM-порт, файл, сетевое соединение и т.д.)
с поддержкой контекстного менеджера.
"""

# System imports
from abc import ABC, abstractmethod

# External imports

# User imports

#########################

# Источник данных с использованием контекстного менеджера
class BytesSource(ABC):
    """Абстрактный базовый класс для источника байтов.

    Определяет интерфейс для чтения байтов и гарантирует корректную
    инициализацию и освобождение ресурсов через протокол контекстного менеджера.

    Методы:
        setup(): Подготовка ресурсов (открытие порта, файла и т.д.).
        cleanup(): Освобождение ресурсов (закрытие порта, файла).
        read_byte() -> bytes: Чтение одного байта.
    """

    @abstractmethod
    def setup(self):
        """Выполняет подготовку ресурсов перед чтением.

        Вызывается автоматически при входе в контекстный менеджер (`with`).
        Должен быть реализован в наследнике.
        """
        pass

    @abstractmethod
    def cleanup(self):
        """Освобождает ресурсы после завершения работы.

        Вызывается автоматически при выходе из контекстного менеджера.
        Должен быть реализован в наследнике.
        """
        pass

    @abstractmethod
    def read_byte(self) -> bytes:
        """Читает один байт из источника.

        Returns:
            bytes: Прочитанный байт (объект bytes длины 1).

        Raises:
            ReadError: Если произошла ошибка чтения (таймаут, потеря соединения,
                достигнут конец файла и т.п.). Наследники могут выбрасывать
                более специфичные исключения, унаследованные от `ReadError`.
        """
        pass

    def __enter__(self):
        """Вход в контекстный менеджер.

        Вызывает `setup()` и возвращает экземпляр источника.
        """
        self.setup()
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        """Выход из контекстного менеджера.

        Вызывает `cleanup()`. Возвращает `False`, чтобы исключения,
        возникшие внутри блока `with`, пробрасывались дальше.
        """
        self.cleanup()
        return False