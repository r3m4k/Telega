/* *****************************************************************************
Принцип работы фильтра:

Принцип его работы заключается в последовательной двухуровневой обработке 
поступающих данных. На первом этапе входящий поток значений разбивается на
непрерывные пакеты фиксированной длины, составляющей 16 отсчётов. Для каждого
такого пакета независимо рассчитываются две ключевые статистические 
характеристики: среднее арифметическое и среднеквадратическое отклонение. 
Эти показатели позволяют оценить не только центральную тенденцию сигнала на
коротком интервале, но и степень его разброса. Далее применяется пороговое
правило для исключения аномальных значений: все отсчёты внутри пакета, модуль
отклонения которых от рассчитанного среднего превышает удвоенное значение
среднеквадратического отклонения, признаются статистическими выбросами и 
отбрасываются из дальнейшей обработки (правило 2σ). Таким образом, на данном
этапе происходит первичная адаптивная очистка данных внутри каждого окна 
наблюдения. На втором, агрегирующем этапе, оставшиеся после отсева значения
из четырёх последовательных пакетов объединяются в общую выборку.

По этой совокупности «очищенных» данных вычисляется итоговое среднее
значение, которое и является выходным результатом фильтра, передаваемым
для последующего анализа на персональный компьютер.
***************************************************************************** */

